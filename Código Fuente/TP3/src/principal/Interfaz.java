    private void jMenuItem1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem1ActionPerformed
        ProtectionDomain pd = Interfaz.class.getProtectionDomain();
        CodeSource cs = pd.getCodeSource();
        URL location = cs.getLocation();
        
        JFileChooser openImage = new JFileChooser(new File("."));

		openImage.setDialogTitle("Cargar modelo");
		openImage.setApproveButtonText("Cargar modelo");

		// Solo permitimos abrir huellas digitales
		FileNameExtensionFilter filter = new FileNameExtensionFilter("Archivo .SUR", "sur");
		openImage.setFileFilter(filter);

		if (openImage.showOpenDialog(this) == JFileChooser.APPROVE_OPTION) {
			File image = openImage.getSelectedFile();
			if (!image.canRead()) {
				image.setReadable(true);
			}
                archivo = image;
                //botonDibujar.setEnabled(true);
		}
             try {
                this.leerArchivo();
                labelArchivo.setText(archivo.getName());
                labelTriangulos.setText(new Integer(figura.getCantTriangulos()).toString());
            } catch (IOException ex) {
                Logger.getLogger(Interfaz.class.getName()).log(Level.SEVERE, null, ex);
            }
    }//GEN-LAST:event_jMenuItem1ActionPerformed

