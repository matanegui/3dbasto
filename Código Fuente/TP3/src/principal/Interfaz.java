    public BufferedImage getImagen(){
        BufferedImage img = new BufferedImage(panel.getWidth(), panel.getHeight(), BufferedImage.TYPE_INT_RGB);
	Graphics g = img.createGraphics();
	
        Color alambre = new Color(255,127,39);        //naranja del alambre
        Color fill = new Color(0,0,0);               //fill del poligono
        
        
        int xpoints[] = {0,0,0};
        int ypoints[]= {0,0,0};
        
        for (Triangle t : figura.getTriangulos()){
            
            
            xpoints[0] = this.mapearPuntoX(figura.getVertice(t.getV1()).getX());
            xpoints[1] = this.mapearPuntoX(figura.getVertice(t.getV2()).getX());
            xpoints[2] = this.mapearPuntoX(figura.getVertice(t.getV3()).getX());
            
            
            ypoints[0] = this.mapearPuntoY(figura.getVertice(t.getV1()).getY());
            ypoints[1] = this.mapearPuntoY(figura.getVertice(t.getV2()).getY());
            ypoints[2] = this.mapearPuntoY(figura.getVertice(t.getV3()).getY());
     
            //pintar poligono
            
            //angulo entre luz y cada poligono
            
            double ang = dir_normales*t.getNormal(figura.getVertices()).getCosAnguloCon(luz);
            double intens;
            if (ang<0){
                intens=0;
            }
            else{
                intens=ang;
            }
            fill = new Color((int)(colorMesh.getRed()*intens),(int)(colorMesh.getGreen()*intens),(int)(colorMesh.getBlue()*intens)); 
            
            Polygon pol = new Polygon(xpoints, ypoints, 3);
            g.setColor(fill);
            g.fillPolygon(pol);
            
            if (malla){
                g.setColor(alambre);
                g.drawPolygon(pol);
            }
        }
        return img;
    }
    
    public int mapearPuntoX(double posx){
        return ((int)(ancho * (posx)));
        
    }
    
    public int mapearPuntoY(double posy){
        return ((int)(alto * (1-posy)));
    }
    
