    private void panelMouseDragged(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_panelMouseDragged
        if ( evt.isMetaDown() ) {
			//aprete boton derecho
                        
			Vertex direccion = new Vertex((evt.getY() - posClickInicial.getY()) * 0.005,(evt.getX() - posClickInicial.getX()) * 0.005, 0);
                        figura.rotar(direccion);	
		}
		else{
                         Vertex direccion = new Vertex((evt.getX() - posClickInicial.getX())/dimPanel*figura.getDimension(), -(evt.getY() - posClickInicial.getY())/dimPanel*figura.getDimension(), 0);
			figura.transladar(direccion);	
		}
		dibujar();
		posClickInicial.setX(evt.getX());
		posClickInicial.setY(evt.getY());
    }//GEN-LAST:event_panelMouseDragged

